					<h2 class="text-underline">Why is Eastern State Undertaking this Project?</h2>
				</div>
				<p>
					Eastern State Penitentiary is committed to deepening the conversation about real criminal justice reform in the United States.
					With <em>Hidden Lives Illuminated</em>, the organization can structure these conversations around the literal voices of men and 
					women living inside prisons today. “Correctional institutions are some of the most inaccessible spaces in America,” said Senior 
					Vice President Sean Kelley.  “We want to use our wall to illuminate the lives of people living inside these institutions that are 
					so often misunderstood, or, worse, ignored.”
				</p>
				
				
